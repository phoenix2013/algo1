package arrayinversion;

import java.math.BigInteger;

public class CountWrapper {
	public CountWrapper(BigInteger count) {
		super();
		this.count = count;
	}

	BigInteger count;

	public BigInteger getCount() {
		return count;
	}

	public void setCount(BigInteger count) {
		this.count = count;
	}
	
	
}
